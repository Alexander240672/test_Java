package com.Alexander.day3;

public class modulus {
    public static void main(String[] args)
    {
        //% 取模
        //% 本质：a % b = a - a / b * b
        System.out.println(10 % 3);
        System.out.println(-10 % 3);
        System.out.println(10 % -3);
        System.out.println(-10 % -3);
    }
}
